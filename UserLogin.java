package com.bean;

public class UserLogin {
	private String name, email, phone, passwod, cpasswod, key, keyma;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getPasswod() {
		return passwod;
	}

	public void setPasswod(String passwod) {
		this.passwod = passwod;
	}

	public String getCpasswod() {
		return cpasswod;
	}

	public void setCpasswod(String cpasswod) {
		this.cpasswod = cpasswod;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getKeyma() {
		return keyma;
	}

	public void setKeyma(String keyma) {
		this.keyma = keyma;
	}
	

}
